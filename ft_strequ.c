int		ft_strequ(char const *s1, char const *s2)
{
	int i;
	int	c1;
	int	c2;

	i = 0;
	c1 = 0;
	c2 = 0;
	if (*s1 == NULL || *s2 == NULL)
		return (0);
	while (s1[i] != '\0' || s2[i] != '\0')
	{
		c1 = s1[i] + c1;
		c2 = s2[i] + c2;
		i++;
	}
	if (c1 == c2)
		return (1);
	return (0);
}
