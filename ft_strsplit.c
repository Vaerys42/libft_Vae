char	**ft_strsplit(char *s, char c)
{
	int		i;
	int		j;
	int		k;
	char	**new;

	i = 0;
	j = 0;
	k = 0;
	while(s[i] != '\0')
	{
		if (s[i] == c)
		{
			while (s[i] == c)
				i++;
			new[j][k] = '\0';
			j++;
		}
		new[j][k] = s[i];
		i++;
	}
	return (new);
}
